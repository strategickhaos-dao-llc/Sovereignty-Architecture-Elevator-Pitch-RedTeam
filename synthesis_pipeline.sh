                ;;
            -t|--topic)
                TOPIC="$2"
                shift 2
                ;;
            -o|--output)
                OUTPUT_DIR="$2"
                shift 2
                ;;
            -f|--framework)
                IP_FRAMEWORK="$2"
                shift 2
                ;;
            --skip-ipfs)
