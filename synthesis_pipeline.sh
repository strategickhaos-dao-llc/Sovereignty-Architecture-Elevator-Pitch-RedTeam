    # Parse arguments
    INPUT_FILES=()
    TOPIC=""
    SKIP_IPFS="false"
    SKIP_OTS="false"
    SKIP_GRAPH="false"
    
    while [[ $# -gt 0 ]]; do
        case $1 in
            -i|--inputs)
                IFS=',' read -ra INPUT_FILES <<< "$2"
